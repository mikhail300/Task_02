public class Task03 {
// 3. Необходимо вывести на экран таблицу умножения на 3:
	public static void task() {
		System.out.println("3. Необходимо вывести на экран таблицу умножения на 3:");

		int i;

		System.out.println();

		for (i = 0; i <= 9; i++) {

			System.out.println("\t" + "3 * " + i + " = " + (3 * i));

		}
		System.out.println();
	}
}
