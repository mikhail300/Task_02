package by.htp.les05.cycle;

public class Task01 {
// 1. Необходимо вывести на экран числа от 1 до 5.
	public static void task() {
		System.out.println("1. Необходимо вывести на экран числа от 1 до 5.");
		
		int i;
		
		System.out.println ();
		
		for (i = 1; i <= 5 ; i ++) {
			
			System.out.print("\t" + i);
			
		}
		System.out.println();
		System.out.println();
	}
}
