package by.htp.les05.cycle;

public class Task02 {
// 2. Необходимо вывести на экран числа от 5 до 1.
	public static void task() {
		System.out.println("2. Необходимо вывести на экран числа от 5 до 1.");

		int i;

		System.out.println();

		for (i = 5; i >= 1; i--) {

			System.out.print("\t" + i);

		}
		System.out.println();
		System.out.println();
	}
}
